package de.ruu.app.jeeeraaah.client.fx.taskgroup.mapstruct;

import de.ruu.app.jeeeraaah.common.Task;
import de.ruu.app.jeeeraaah.common.jpadto.TaskGroupEntity;
import de.ruu.lib.util.Strings;
import jakarta.annotation.Nullable;
import lombok.AccessLevel;
import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.NonNull;
import lombok.Setter;
import lombok.ToString;
import lombok.experimental.Accessors;
import lombok.extern.slf4j.Slf4j;
import org.mapstruct.AfterMapping;
import org.mapstruct.BeforeMapping;

import java.util.Collections;
import java.util.HashSet;
import java.util.Objects;
import java.util.Optional;
import java.util.Set;

import static java.util.Objects.isNull;

/** JavaBean for implementing business logic */
@EqualsAndHashCode
@ToString(callSuper = true)
@Slf4j
@Getter                   // generate getter methods for all fields using lombok unless configured otherwise
@Setter                   // generate setter methods for all fields using lombok unless configured otherwise
@Accessors(fluent = true) // generate fluent accessors with lombok and java-bean-style-accessors in non-abstract classes
                          // with ide, fluent accessors will (usually / by default) be ignored by mapstruct
public class TaskGroupBean implements TaskGroupEntity<TaskBean>
{
	@Setter(AccessLevel.NONE) @Nullable private Long  id;
	@Setter(AccessLevel.NONE) @Nullable private Short version;

	/** mutable non-null */
	// no lombok-generation of setter because of additional validation in manually created method
	@Setter(AccessLevel.NONE)
	@NonNull  private String name;

	/** mutable nullable */
	@Nullable private String description;

	/**
	 * prevent direct access to this modifiable set from outside this class, use {@link #addTask(TaskBean)} and
	 * {@link #removeTask(TaskBean)} to modify the set
	 * <p>
	 * may explicitly be {@code null}, {@code null} indicates that there was no attempt to load related objects from db
	 * (lazy)
	 */
	@EqualsAndHashCode.Exclude
	@ToString.Exclude
	@Getter(AccessLevel.NONE) // provide handmade getter that returns unmodifiable
	@Setter(AccessLevel.NONE) // no setter at all
	@Nullable private Set<TaskBean> tasks;

	///////////////
	// constructors
	///////////////

	/**
	 * provide hand-made required args constructor to guarantee usage of hand made accessors.
	 * @param name non-empty name
	 */
	public TaskGroupBean(@NonNull String name) { name(name); }

	////////////////////////////////////////////////////////////////////////
	// fluent style accessors generated by lombok if not specified otherwise
	////////////////////////////////////////////////////////////////////////

	/**
	 * manually created fluent setter with extra parameter check (see throws documentation)
	 * @param name non-null, non-empty, non-blank
	 * @return {@code this}
	 * @throws IllegalArgumentException if {@code name} parameter is empty or blank
	 * @throws NullPointerException     if {@code name} parameter is {@code null}
	 */
	@Override @NonNull public TaskGroupBean name(@NonNull String name)
	{
		if (Strings.isEmptyOrBlank(name)) throw new IllegalArgumentException("name must not be empty nor blank");
		this.name = name;
		return this;
	}

	@Override @NonNull public Optional<String> description() { return Optional.ofNullable(description); }

	/** @return optional unmodifiable */
	@Override public @NonNull Optional<Set<TaskBean>> tasks()
	{
		if (Objects.isNull(tasks)) return Optional.empty();
		return Optional.of(Collections.unmodifiableSet(tasks));
	}

	///////////////////////
	// additional accessors
	///////////////////////

	// --- none ---

	////////////////////////
	// relationship handling
	////////////////////////
	/**
	 * @param bean the {@link Task} to be added as predecessor
	 * @return {@code this}
	 */
	@Override public boolean addTask(@NonNull TaskBean bean)
	{
		if (nonNullTasks().add(bean))
		{
			// update bidirectional relation
			bean.taskGroup(this);
			return true;
		}
		return false;
	}

	@Override public boolean removeTask(@NonNull TaskBean bean)
	{
		return tasks().map(ts -> ts.remove(bean)).orElse(false);
	}

	//////////////////////
	// mapstruct callbacks
	//////////////////////

	/** {@link TaskGroupDTO} -> {@link TaskGroupBean} */
	@BeforeMapping void beforeMapping(@NonNull TaskGroupDTO source)
	{
		id      = source.id     ();
		version = source.version();

		source.taskDTOs().ifPresent(ts -> ts.forEach(t -> addTask(Map_Task_DTO_Bean.INSTANCE.map(t))));
	}

	/** {@link TaskGroupDTO} -> {@link TaskGroupBean} */
	@AfterMapping void afterMapping(@NonNull TaskGroupDTO source) { }

	@NonNull public TaskGroupDTO toDTO() { return Map_TaskGroup_DTO_Bean.INSTANCE.map(this); }

	/** {@link TaskGroupFXBean} -> {@link TaskGroupBean} */
	@BeforeMapping void beforeMapping(@NonNull TaskGroupFXBean source)
	{
		id      = source.id     ();
		version = source.version();

//		source.tasks().ifPresent(vs -> vs.forEach(v -> addTask(Map_TaskGroup_Bean_FXBean.INSTANCE.lookupOrCreate(v))));
		source.tasks().ifPresent(vs -> vs.forEach(v -> addTask(v.toBean())));
	}

	/** {@link TaskGroupFXBean} -> {@link TaskGroupBean} */
	@AfterMapping void afterMapping(@NonNull TaskGroupFXBean source) { }

	@NonNull public TaskGroupFXBean toFXSource() { return Map_TaskGroup_Bean_FXBean.INSTANCE.map(this); }

	@BeforeMapping public void afterMapping(@NonNull TaskGroupBean source) { }

	//////////////////////////////////////////////////////////////////////////////////////////////
	// java bean style accessors for those who do not work with fluent style accessors (mapstruct)
	//////////////////////////////////////////////////////////////////////////////////////////////

	@NonNull
	public String getName()                     { return name(); }
	public void   setName(@NonNull String name) {   name(name);  }

	@Nullable
	public String getDescription()                             { return description().orElse(null); }
	public void   setDescription(@Nullable String description) { description(description); }
	// do _NOT_ define getter for tasks to avoid handling of tasks by mapstruct automatism

	private @NonNull Set<TaskBean> nonNullTasks()
	{
		if (isNull(tasks)) tasks = new HashSet<>();
		return tasks;
	}
}